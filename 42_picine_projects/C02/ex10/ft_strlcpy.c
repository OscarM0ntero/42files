unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	tam;
	unsigned int	i;

	tam = 0;
	i = 0;
	while (src[tam] != '\0')
		tam++;
	if (size != 0)
	{
		while (src[i] != '\0' && i < (size - 1))
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (tam);
}
